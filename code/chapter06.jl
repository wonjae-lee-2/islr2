schema(dfhitters[:, 13:end])

# ╔═╡ 48a83622-73c9-4c37-888d-1df250ce0f06
y, X = unpack(dfhitters, ==(:Salary))

# ╔═╡ 2678fb0e-a7d8-439e-bf97-1765725761aa
OneHotEncoder = @load ContinuousEncoder pkg = MLJModels verbosity = 0

# ╔═╡ f7711237-2776-4520-a1b0-d427222f8f87
LinearRegressor = @load LinearRegressor pkg = MLJLinearModels verbosity = 0

# ╔═╡ 2166a704-6314-4a9c-8ef7-3b83f97e9521
pipe = Pipeline(
	OneHotEncoder(drop_last = true),
	LinearRegressor()
)

# ╔═╡ 7754acfa-0b0e-4ad3-8afc-78c025719510
mach = machine(pipe, X, y) |> fit!

# ╔═╡ 963526f9-2be5-4af0-ad4a-ed5f5100d1f0
rsquared(predict(mach, X), y)

# ╔═╡ 346737c2-a370-479c-9d86-447254d3b9a6
function subsets_r2(columns)
	X = dfhitters[:, columns]
	y = dfhitters[:, :Salary]
	pipe = Pipeline(
		OneHotEncoder(drop_last = true),
		LinearRegressor()
	)
	mach = machine(pipe, X, y) |> fit!
	return rsquared(predict(mach, X), y)
end

# ╔═╡ 250b7259-98b8-4199-944d-18795a668a71
function subsets_formula(columns)
	strings = map(string, columns)
	return "Salary ~ $(join(strings, " + "))"
end

# ╔═╡ 60163844-a5ae-4145-8e1b-eb2f61d203de
function subsets_all(num_vars)
	columns = [x for x in combinations(filter(x -> x != "Salary", names(dfhitters)), num_vars)]
	return DataFrames.DataFrame(
		"num_vars" => num_vars,
		"formula" => [subsets_formula(x) for x in columns],
		"r2" => [subsets_r2(x) for x in columns]
	)
end

# ╔═╡ 0efc6f21-17d3-42c3-bdd8-4efd7377f65f
dfsubsets = vcat([subsets_all(x) for x in 1:3]...)

# ╔═╡ c2245b8b-623f-4055-bb94-b49b24eba184
DataFrames.combine(sdf -> DataFrames.subset(sdf, :r2 => x -> x .== maximum(x)), DataFrames.groupby(dfsubsets, :num_vars))

